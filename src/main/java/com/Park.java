package com;

public class Park {

    private int xsize;

    private int ysize;

    public Park(int xsize, int ysize) {
        this.xsize = xsize;
        this.ysize = ysize;
    }

    public int getXsize() {
        return xsize;
    }

    public void setXsize(int xsize) {
        this.xsize = xsize;
    }

    public int getYsize() {
        return ysize;
    }

    public void setYsize(int ysize) {
        this.ysize = ysize;
    }
}
